package record;

public class RecordBean {
	private int rec_num;
	private String rec_name;
	private String rec_date;
	private String rec_dis;
	private String rec_time;
	private String rec_diary;
	public int getRec_num() {
		return rec_num;
	}
	public void setRec_num(int rec_num) {
		this.rec_num = rec_num;
	}
	public String getRec_name() {
		return rec_name;
	}
	public void setRec_name(String rec_name) {
		this.rec_name = rec_name;
	}
	public String getRec_date() {
		return rec_date;
	}
	public void setRec_date(String rec_date) {
		this.rec_date = rec_date;
	}
	public String getRec_dis() {
		return rec_dis;
	}
	public void setRec_dis(String rec_dis) {
		this.rec_dis = rec_dis;
	}
	public String getRec_time() {
		return rec_time;
	}
	public void setRec_time(String rec_time) {
		this.rec_time = rec_time;
	}
	public String getRec_diary() {
		return rec_diary;
	}
	public void setRec_diary(String rec_diary) {
		this.rec_diary = rec_diary;
	}
	
	
}
